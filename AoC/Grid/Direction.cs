namespace AoC.Grid;

/// <summary>
/// Represents the directions for traversing a grid.
/// </summary>
public enum Direction
{
    /// <summary>
    /// The south direction.
    /// </summary>
    Down = 0,

    /// <summary>
    /// The west direction.
    /// </summary>
    Left,

    /// <summary>
    /// The east direction.
    /// </summary>
    Right,

    /// <summary>
    /// The north direction.
    /// </summary>
    Up,
}
